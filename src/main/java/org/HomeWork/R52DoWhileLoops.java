package org.HomeWork;

public class R52DoWhileLoops {
    public static void main(String[] args) {
        /*
        Create a do while loop that prints out "Hello World!" 5 times.
         */
        int a = 0;
        do {
            System.out.println("Hello World!");
            a++;
        }
        while (a < 5);
    }
}

