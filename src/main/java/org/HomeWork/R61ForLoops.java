package org.HomeWork;

public class R61ForLoops {
    public static void main(String[] args) {
        /*
        Write a program that uses for loop to
        prints multiplication of  number 3 from 1 to 10
         */
        for (int x = 1; x <= 10; x++){
            System.out.println("3"+"*"+(x)+"="+(3*x));
        }

        for (int x = 1; x <= 10; x++){
            System.out.println("3*"+(x)+"="+(3*(x)));
        }
    }
}
