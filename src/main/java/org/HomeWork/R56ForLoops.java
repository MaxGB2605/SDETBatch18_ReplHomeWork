package org.HomeWork;

public class R56ForLoops {
    public static void main(String[] args) {
        /*
        Create a for loop that prints out even numbers from
         2 to 14 using if condition
Must include the number 14 in the output
         */
        for (int x = 2; x <= 14;x++) {
            if (x % 2==0){
                System.out.println(x);
            }
        }
    }
}
